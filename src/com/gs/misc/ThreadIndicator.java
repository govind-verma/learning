package com.gs.misc;

public class ThreadIndicator {

	private int val;
	private boolean hasDone = true;
	
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	public boolean isHasDone() {
		return hasDone;
	}
	public void setHasDone(boolean hasDone) {
		this.hasDone = hasDone;
	}
	
	
}
